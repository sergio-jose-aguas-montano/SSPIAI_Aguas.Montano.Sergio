% Relacion/Parentesco
padre(P,H) :- progenitor(P,H), hombre(P).
madre(M,H) :- progenitor(M,H), mujer(M).

hijo(H,P) :- progenitor(P,H), hombre(H).
hija(H,P) :- progenitor(P,H), mujer(H).

abuelo(A,N) :- progenitor(A,P), progenitor(P,N), hombre(A).
abuelo_pat(A,N) :- padre(A,P), padre(P,N).
abuelo_mat(A,N) :- padre(A,M), madre(M,N).

abuela(A,N) :- progenitor(A,M), progenitor(M,N), mujer(A).
abuela_pat(A,N) :- madre(A,P), padre(P,N).
abuela_mat(A,N) :- madre(A,M), madre(M,N).

abuelos_pat(Ao,Aa,N) :- abuelo_pat(Ao,N), abuela_pat(Aa,N), Ao\=Aa.
abuelos_mat(Ao,Aa,N) :- abuelo_mat(Ao,N), abuela_mat(Aa,N), Ao\=Aa.

hermanos(H1,H2) :- padre(P,H1), padre(P,H2), H1\=H2.
hermanos(H1,H2) :- madre(P,H1), madre(P,H2), H1\=H2.

tio(T, S) :- (hermanos(T, X), padre(X, S), hombre(T), T\=X) ; (hermanos(T, Y), madre(Y, S), hombre(T), T\=Y).
tia(T, S) :- (hermanos(T, X), madre(X, S), mujer(T), T\=X) ; (hermanos(T, Y), padre(Y, S), mujer(T), T\=Y).

primos(P1, P2) :- progenitor(Pa1, P1), progenitor(Pa2, P2), hermanos(Pa1, Pa2), P1 \= P2.

pareja(Fs,Pa) :- novios(Fs,Pa), Fs\=Pa.

tio_politico(Tp,S) :- pareja(S,Pa), progenitor(Su,Pa), hermanos(Tp,Su), (tio(Tp,Pa);tia(Tp,Pa)), Tp\=Su, Tp\=S.
tia_politica(Tp,S) :- pareja(S,Pa), progenitor(Su,Pa), hermanos(Tp,Su), (tio(Tp,Pa);tia(Tp,Pa)), Tp\=Su, Tp\=S.

tio_abuelo(T,S) :- hermanos(T,Ab), progenitor(Ab,Pa), progenitor(Pa,S), hombre(T), T\=S.
tia_abuela(T,S) :- hermanos(T,Ab), progenitor(Ab,Pa), progenitor(Pa,S), mujer(T), T\=S.

bisabuelo(B,N) :- progenitor(B,Ab), progenitor(Ab,Pa), progenitor(Pa,N), hombre(B).
bisabuela(B,N) :- progenitor(B,Ab), progenitor(Ab,Pa), progenitor(Pa,N), mujer(B).
